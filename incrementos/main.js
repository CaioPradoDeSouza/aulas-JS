let n = 10;


// incrementa depois usa o valor tento para + quanto para -
console.log(++n);
console.log(n);

//usa o valor e depois incrementa tanto para + quanto para -
console.log(n++);
console.log(n);

// soma um valor fixo a n
console.log(n+=10);


// inverte o valor de positivo para negativo
console.log(n=-10);

// inverte o valor de negativo para positivo novamente
console.log(n=+10);

